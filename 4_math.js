// Using Math.PI to get the value of Ï€ (pi)
// Definition: Math.PI is a constant in JavaScript that represents the ratio of the circumference of a circle to its diameter, approximately equal to 3.14159.
const piValue = Math.PI;
console.log("Value of Pi:", piValue);

// Using Math.abs to get the absolute value of -5
// Definition: Math.abs() returns the absolute value of a number, which is the non-negative value of that number without regard to its sign.
const absValue = Math.abs(-5);
console.log("Absolute value of -5:", absValue);

// Using Math.round to round 3.7 to the nearest integer
// Definition: Math.round() rounds a number to the nearest integer. If the fractional part is 0.5 or greater, it rounds up; otherwise, it rounds down.
const roundedValue = Math.round(3.7);
console.log("Rounded value of 3.7:", roundedValue);

// Using Math.ceil to get the ceiling of 3.7
// Definition: Math.ceil() returns the smallest integer greater than or equal to a given number, effectively rounding up to the nearest whole number.
const ceilValue = Math.ceil(3.7);
console.log("Ceiling of 3.7:", ceilValue);

// Using Math.floor to get the floor of 3.7
// Definition: Math.floor() returns the largest integer less than or equal to a given number, effectively rounding down to the nearest whole number.
const floorValue = Math.floor(3.7);
console.log("Floor of 3.7:", floorValue);

// Using Math.trunc to get the integer part of 3.7
// Definition: Math.trunc() returns the integer part of a number, effectively truncating the decimal part
const truncValue = Math.trunc(3.7);
console.log("Truncated value of 3.7:", truncValue);

// Using Math.pow to calculate 2^3
// Definition: Math.pow() returns the result of raising a number to a power, equivalent to the expression
const powValue = Math.pow(2, 3);
console.log("2^3:", powValue);

// Using Math.sqrt to calculate the square root of 16
// Definition: Math.sqrt() returns the square root of a number, equivalent to the expression
const sqrtValue = Math.sqrt(16);
console.log("Square root of 16:", sqrtValue);

// Using Math.cbrt to calculate the cube root of 27
// Definition: Math.cbrt() returns the cube root of a number, equivalent to the expression
const cbrtValue = Math.cbrt(27);
console.log("Cube root of 27:", cbrtValue);

// Using Math.log to calculate the natural logarithm of 10
// Definition: Math.log() returns the natural logarithm of a number, equivalent to the expression
const logValue = Math.log(10);
console.log("Natural logarithm of 10:", logValue);

